console.log("The value of PI floor is " + Math.floor(pi));
console.log("The value of PI ceiling is " + Math.ceil(pi));

console.log("This square root of PI is " + Math.sqrt(pi));
console.log("PI to the power of 2 is " + Math.pow(pi, 2));

